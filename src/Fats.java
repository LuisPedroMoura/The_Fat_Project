
public class Fats {

	private double fats;
	private double saturated;
	private double monounsaturated;
	private double polyunsaturated;
	private double linoleic;
	private double trans;
	
	public int getFats() {
		return fats;
	}
	public void setFats(int fats) {
		this.fats = fats;
	}
	public int getSaturated() {
		return saturated;
	}
	public void setSaturated(int saturated) {
		this.saturated = saturated;
	}
	public int getMonounsaturated() {
		return monounsaturated;
	}
	public void setMonounsaturated(int monounsaturated) {
		this.monounsaturated = monounsaturated;
	}
	public int getPolyunsaturated() {
		return polyunsaturated;
	}
	public void setPolyunsaturated(int polyunsaturated) {
		this.polyunsaturated = polyunsaturated;
	}
	public int getLinoleic() {
		return linoleic;
	}
	public void setLinoleic(int linoleic) {
		this.linoleic = linoleic;
	}
	public int getTrans() {
		return trans;
	}
	public void setTrans(int trans) {
		this.trans = trans;
	}
	
	
	
}
