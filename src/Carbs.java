
public class Carbs {
	
	private double carbs;
	private double saccharides;
	private double oligosaccharides;
	private double starch;
	
	public int getCarbs() {
		return carbs;
	}
	public void setCarbs(int carbs) {
		this.carbs = carbs;
	}
	public int getSaccharides() {
		return saccharides;
	}
	public void setSaccharides(int saccharides) {
		this.saccharides = saccharides;
	}
	public int getOligosaccharides() {
		return oligosaccharides;
	}
	public void setOligosaccharides(int oligosaccharides) {
		this.oligosaccharides = oligosaccharides;
	}
	public int getStarch() {
		return starch;
	}
	public void setStarch(int starch) {
		this.starch = starch;
	}
	
	
	
}
